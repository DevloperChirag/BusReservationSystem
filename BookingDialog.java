private void updateSeatAndConfirm() {
    String userName = nameField.getText().trim();
    String contact = phoneField.getText().trim();
    // Validate user inputs
    if (userName.isEmpty() || contact.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter your name and contact number!", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    try (Connection conn = DatabaseConnection.getConnection()) {
        // Step 1: Update Seat Count
        String updateQuery = "UPDATE bus_details SET seat = seat - 1 WHERE bus_no = ?";
        PreparedStatement stmt = conn.prepareStatement(updateQuery);
        stmt.setString(1, busNo);
        int rows = stmt.executeUpdate();

        if (rows > 0) {
            // Step 2: Insert Booking Details into `bookings` Table
            String insertQuery = "INSERT INTO bookings (user_name, contact, bus_no, source, destination, price, seat_no, time, date, payment_status) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?)";

            PreparedStatement insertStmt = conn.prepareStatement(insertQuery);
            insertStmt.setString(1, userName);
            insertStmt.setString(2, contact);
            insertStmt.setString(3, busNo);
            insertStmt.setString(4, source);
            insertStmt.setString(5, destination);
            insertStmt.setDouble(6, price);
            insertStmt.setInt(7, seat);
            insertStmt.setString(8, time);
            insertStmt.setString(9, date);
            insertStmt.setString(10, "Paid");  // Assume payment is successful

            insertStmt.executeUpdate();

            JOptionPane.showMessageDialog(this, 
                "Booking Successful!\nBus NO: " + busNo, 
                "Success", 
                JOptionPane.INFORMATION_MESSAGE
            );

            // Refresh Dashboard Table
            
            dispose(); // Close the dialog
        } else {
            JOptionPane.showMessageDialog(this, 
                "Error updating seat count!", 
                "Error", 
                JOptionPane.ERROR_MESSAGE
            );
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, 
            "Database Error: " + e.getMessage(), 
            "Error", 
            JOptionPane.ERROR_MESSAGE
        );
    }
}

