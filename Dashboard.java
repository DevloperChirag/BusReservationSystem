    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String source = sourceTF.getText().trim();
String destination = destinationTF.getText().trim();

if (source.isEmpty() || destination.isEmpty()) {
    JOptionPane.showMessageDialog(this, "Please enter both source and destination!", "Error", JOptionPane.ERROR_MESSAGE);
    return;
}

String query = "SELECT * FROM bus_details WHERE source = ? AND destination = ?";
try (Connection conn = DatabaseConnection.getConnection();
     PreparedStatement stmt = conn.prepareStatement(query)) {

    stmt.setString(1, source);
    stmt.setString(2, destination);

    ResultSet rs = stmt.executeQuery();

     String[] columnNames = {"Bus No", "Source", "Destination", "Price", "Seat", "Time", "Date","Action"};
    DefaultTableModel model = new DefaultTableModel(columnNames, 0) // Set column name
    {
            @Override
            public boolean isCellEditable(int row, int column) {
                return column == 7; // Make only the "Book" button column editable
            }
        };
    

    while (rs.next()) {
        // Ensure commas (,) are used instead of semicolons (;)
        Object[] row = {
            rs.getString("bus_no"),
            rs.getString("source"),
            rs.getString("destination"),
            rs.getDouble("price"), // Assuming price is stored as a numeric value
            rs.getInt("seat"),     // Assuming seat is stored as an integer
            rs.getString("time"),
            rs.getString("date"),
            "Book"
        };
        model.addRow(row);
    }
        bustable.setModel(model);
        bustable.getColumn("Action").setCellRenderer(new ButtonRenderer());
        bustable.getColumn("Action").setCellEditor(new ButtonEditor(new JCheckBox()));

        bustable.repaint();

} catch (SQLException e) {
    JOptionPane.showMessageDialog(this, "Database Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
}

        
        
    }//GEN-LAST:event_jButton1ActionPerformed
