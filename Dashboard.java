// Custom editor to handle button clicks
class ButtonEditor extends DefaultCellEditor {
    private JButton button;
    private String busNo;
    private int seat;
    private double price;
    private String source, destination, time, date;

    public ButtonEditor(JCheckBox checkBox) {
        super(checkBox);
        button = new JButton("Book");
        button.setOpaque(true);

        button.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                int selectedRow = bustable.getSelectedRow();
                if (selectedRow != -1) {
                    // Get bus details from the selected row
                    busNo = bustable.getValueAt(selectedRow, 0).toString();
                    source = bustable.getValueAt(selectedRow, 1).toString();
                    destination = bustable.getValueAt(selectedRow, 2).toString();
                    price = Double.parseDouble(bustable.getValueAt(selectedRow, 3).toString());
                    seat = Integer.parseInt(bustable.getValueAt(selectedRow, 4).toString());
                    time = bustable.getValueAt(selectedRow, 5).toString();
                    date = bustable.getValueAt(selectedRow, 6).toString();
                    
                    // Open the booking form
                    BookingDialog bookingDialog = new BookingDialog(null, true, busNo, source, destination, price, seat, time, date);
                    bookingDialog.setVisible(true);
                }
            }
        });
    }

    @Override
    public Component getTableCellEditorComponent(JTable table, Object value, boolean isSelected, int row, int column) {
        return button;
    }
}

